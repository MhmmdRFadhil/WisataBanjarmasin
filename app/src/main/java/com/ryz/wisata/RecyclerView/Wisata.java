package com.ryz.wisata.RecyclerView;

public class Wisata {

    private String tvList, tvDetail;
    private int bgList;

    public String getTvList() {
        return tvList;
    }

    public void setTvList(String tvList) {
        this.tvList = tvList;
    }

    public String getTvDetail() {
        return tvDetail;
    }

    public void setTvDetail(String tvDetail) {
        this.tvDetail = tvDetail;
    }

    public int getBgList() {
        return bgList;
    }

    public void setBgList(int bgList) {
        this.bgList = bgList;
    }

}
